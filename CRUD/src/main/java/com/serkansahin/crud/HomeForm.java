    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        String searchText = txtSearch.getText();
        TableRowSorter<DefaultTableModel> rowSorter = new TableRowSorter<DefaultTableModel>(tblCitiesModel);
        tblCities.setRowSorter(rowSorter);
        rowSorter.setRowFilter(RowFilter.regexFilter(searchText));
    }//GEN-LAST:event_txtSearchKeyReleased

    private void btnCityAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCityAddActionPerformed

        String name = txtCreateOperationCityName.getText();
        String distrcit = txtCreateOperationCityDistrict.getText();
        int population = Integer.parseInt(txtCreateOperationCityPopulation.getText());

        City city = new City(name, distrcit, population);
        String resultMessage = cityService.AddCity(city);
        lblResult.setText(resultMessage);
        
        getTblCityContent(cityService.GetCities());
    }//GEN-LAST:event_btnCityAddActionPerformed

    private void tblCitiesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCitiesMouseClicked
        int rowIndex = tblCities.getSelectedRow();
        City city = getSelectedCity(rowIndex);

        lblUpdateOperationCityId.setText(Integer.toString(city.getId()));
        txtUpdateOperationCityName.setText(city.getName());
        txtUpdateOperationCityDistrict.setText(city.getDistrict());
        txtUpdateOperationCityPopulation.setText(Integer.toString(city.getPopulation()));
    }//GEN-LAST:event_tblCitiesMouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int cityId = Integer.parseInt(lblUpdateOperationCityId.getText());
        String result = cityService.Remove(cityId);
        lblResult.setText(result);
        
        getTblCityContent(cityService.GetCities());
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int id = Integer.parseInt(lblUpdateOperationCityId.getText());
        String name = txtUpdateOperationCityName.getText();
        String district = txtUpdateOperationCityDistrict.getText();
        int population = Integer.parseInt(txtUpdateOperationCityPopulation.getText());
        City city = new City(id, name, district, population);

        String result = cityService.Update(city);
        lblResult.setText(result);
        
        getTblCityContent(cityService.GetCities());
    }//GEN-LAST:event_jButton3ActionPerformed

    private City getSelectedCity(int rowIndex) {
        int cityId = Integer.parseInt(tblCities.getValueAt(rowIndex, 0).toString());
        String cityName = tblCities.getValueAt(rowIndex, 1).toString();
        String cityDistrict = tblCities.getValueAt(rowIndex, 2).toString();
        int cityPopulation = Integer.parseInt(tblCities.getValueAt(rowIndex, 3).toString());
        return new City(cityId, cityName, cityDistrict, cityPopulation);
    }

