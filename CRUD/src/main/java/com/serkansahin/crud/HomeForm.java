    private void getTblCityContent(ArrayList<City> cities) {
        tblCitiesModel.setRowCount(0);
        tblCities.setModel(tblCitiesModel);
        for (City city : cities) {
            Object[] row = {city.getId(), city.getName(), city.getDistrict(), city.getPopulation()};
            tblCitiesModel.addRow(row);
        }
    }

